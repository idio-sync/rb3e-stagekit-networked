// URL Decoder
void url_decode(char *dst, const char *src) {
    char a, b;
    while (*src) {
        if (*src == '%' && ((a = src[1]) && (b = src[2]))) {
            if (a >= 'a') a -= 'a'-'A'; if (a >= 'A') a -= ('A' - 10); else a -= '0';
            if (b >= 'a') b -= 'a'-'A'; if (b >= 'A') b -= ('A' - 10); else b -= '0';
            *dst++ = 16 * a + b; src += 3;
        } else if (*src == '+') {
            *dst++ = ' '; src++;
        } else {
            *dst++ = *src++;
        }
    }
    *dst = '\0';
}

// Save credentials to LittleFS
void save_wifi_config(const char *ssid, const char *password) {
    lfs_t *lfs = littlefs_get();
    lfs_file_t file;
    
    // We open/create "settings.toml"
    int err = lfs_file_open(lfs, &file, "settings.toml", LFS_O_WRONLY | LFS_O_CREAT | LFS_O_TRUNC);
    if (err < 0) {
        printf("AP: Failed to open file for writing (%d)\n", err);
        return;
    }

    char file_content[256];
    // Format matches config_parser.c expectations
    snprintf(file_content, sizeof(file_content), 
        "# Auto-generated by AP Setup\n"
        "CIRCUITPY_WIFI_SSID = \"%s\"\n"
        "CIRCUITPY_WIFI_PASSWORD = \"%s\"\n", 
        ssid, password);
        
    lfs_file_write(lfs, &file, file_content, strlen(file_content));
    lfs_file_close(lfs, &file);
    printf("AP: Config saved to settings.toml\n");
}

err_t http_callback(void *arg, struct tcp_pcb *tpcb, struct pbuf *p, err_t err) {
    if (!p) { 
        tcp_close(tpcb); 
        return ERR_OK; 
    }
    
    char *req = (char *)p->payload;
    
    // Check if this is the Save request: GET /save?s=SSID&p=PASS
    if (strncmp(req, "GET /save", 9) == 0) {
        char ssid[64] = {0}; 
        char pass[64] = {0};
        
        char *s_ptr = strstr(req, "s=");
        char *p_ptr = strstr(req, "p=");
        
        if (s_ptr && p_ptr) {
            char *end;
            
            s_ptr += 2; // Skip "s="
            end = strchr(s_ptr, '&');
            if (end) *end = 0; 
            url_decode(ssid, s_ptr);
            
            p_ptr += 2; // Skip "p="
            end = strchr(p_ptr, ' '); // End of HTTP line
            if (end) *end = 0;
            url_decode(pass, p_ptr);
            
            printf("AP: Received credentials for '%s'\n", ssid);
            save_wifi_config(ssid, pass);
            
            // Send success page
            tcp_write(tpcb, html_success, strlen(html_success), TCP_WRITE_FLAG_COPY);
            tcp_output(tpcb);
            
            printf("AP: Rebooting...\n");
            watchdog_enable(2000, 1);
        }
    } else {
        // Send the Form
        tcp_write(tpcb, html_form, strlen(html_form), TCP_WRITE_FLAG_COPY);
    }
    
    pbuf_free(p);
    return ERR_OK;
}

err_t connection_callback(void *arg, struct tcp_pcb *newpcb, err_t err) {
    tcp_recv(newpcb, http_callback);
    return ERR_OK;
}

void run_ap_setup_mode(void) {
    printf("\nENTERING AP SETUP MODE\n");
    
    uint8_t mac[6];
    char ssid[32];
    
    // Ensure WiFi chip is on
    if (cyw43_wifi_get_mac(&cyw43_state, CYW43_ITF_AP, mac) == 0) {
        snprintf(ssid, sizeof(ssid), "StageKit-Setup-%02X%02X%02X", mac[3], mac[4], mac[5]);
    } else {
        sprintf(ssid, "StageKit-Setup-Default");
    }
    
    // Start AP
    cyw43_arch_enable_ap_mode(ssid, AP_PASSWORD, CYW43_AUTH_WPA2_AES_PSK);
    printf("AP Started: '%s' (Pass: %s)\n", ssid, AP_PASSWORD);

    // Allow time for AP interface to initialize
    sleep_ms(100);

    // ============================================
    // CONFIGURE AP NETWORK INTERFACE WITH STATIC IP
    // ============================================
    ip4_addr_t gw, mask;
    IP4_ADDR(&gw, 192, 168, 4, 1);      // AP's IP address
    IP4_ADDR(&mask, 255, 255, 255, 0);  // Subnet mask

    cyw43_arch_lwip_begin();

    // Get the AP network interface (CYW43_ITF_AP = 1)
    struct netif *ap_netif = &cyw43_state.netif[CYW43_ITF_AP];

    // Debug: Check netif state before configuration
    printf("AP netif before config - up:%d link_up:%d\n",
           netif_is_up(ap_netif), netif_is_link_up(ap_netif));

    // Set the IP address, netmask, and gateway
    netif_set_addr(ap_netif, &gw, &mask, &gw);

    // Ensure interface is up
    netif_set_up(ap_netif);
    netif_set_link_up(ap_netif);

    // Set as default netif for AP operations
    netif_set_default(ap_netif);

    // Debug: Verify configuration
    printf("AP netif after config - up:%d link_up:%d IP:%s\n",
           netif_is_up(ap_netif), netif_is_link_up(ap_netif),
           ip4addr_ntoa(netif_ip4_addr(ap_netif)));

    cyw43_arch_lwip_end();

    // ============================================
    // START DHCP SERVER
    // ============================================
    cyw43_arch_lwip_begin();
    dhcp_server_init(&dhcp_server, &gw, &mask);
    cyw43_arch_lwip_end();
    printf("DHCP server started\n");
    // ============================================
    
    // Start Web Server
    cyw43_arch_lwip_begin();
    struct tcp_pcb *pcb = tcp_new();
    tcp_bind(pcb, IP_ADDR_ANY, 80);
    pcb = tcp_listen(pcb);
    tcp_accept(pcb, connection_callback);
    cyw43_arch_lwip_end();

    printf("Web server listening on port 80. Waiting for user...\n");
    printf("Connect to WiFi '%s' and open http://192.168.4.1\n", ssid);
    
    while(true) {
        // Blink fast (triple blink) to indicate AP Mode
        for(int i=0; i<3; i++) {
            cyw43_arch_gpio_put(CYW43_WL_GPIO_LED_PIN, 1);
            sleep_ms(100);
            cyw43_arch_gpio_put(CYW43_WL_GPIO_LED_PIN, 0);
            sleep_ms(100);
        }
        sleep_ms(500);
        watchdog_update();
    }
}